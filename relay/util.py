def sanitize_username(username):
  return re.sub('\W', '', username.lower())[:15]


def extract_url(text):
  if not text:
    return
  url_matcher = re.compile(URL_REGEX)
  result = url_matcher.findall(text)
  url = result[0] if len(result) > 0 else None
  return add_http(url)


def add_http(url):
  logging.info('the url we are adding: %s'%(url))
  protocol_sub = url[:7]
  if protocol_sub != 'http://' and protocol_sub != 'https:/':
    url = 'http://' + url
  logging.info('we did this to it: %s'%(url))
  return url


def make_relay_map(relay):
  return {
      'title': relay.title,
      'description': relay.description,
      'image': relay.image,
      'favicon': relay.favicon,
      'site': relay.site,
      'url': relay.key.id(),
      'kind': relay.kind
  }


def make_sent_relay_map(sent_relay):
  relay = sent_relay.relay.get()
  sent_relay_map = make_relay_map(relay)
  sent_relay_map.update(
    {
      'id': sent_relay.key.id(),
      'sender': sent_relay.sender,
      'timestamp': time.mktime(sent_relay.timestamp.timetuple())
    }
  )
  return sent_relay_map
